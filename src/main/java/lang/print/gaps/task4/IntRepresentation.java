package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int c = 99;
        int h = 104;
        int a = 97;
        int r = 114;
        char k =(char)c;
        char l =(char)h;
        char n =(char)a;
        char m =(char)r;

        System.out.println(k + l + n + m);
    }
}
